void main()
{
  var age = 18;
  switch(age)
  {
    case 16:
      print("You are 16 years old.");
      break;
    case 18:
      print("You are 18 years old.");
      break;
    case 21:
      print("You are 21 years old.");
      break;
    default:
      print("Age not matched.");
  }
}